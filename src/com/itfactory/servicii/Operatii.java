package com.itfactory.servicii;

public class Operatii
{
    public int adunareInt(int a, int b)
    {
        return a+b;
    }

    public int scadereInt(int a, int b)
    {
        return a-b;
    }

    public int inmultireInt(int a, int b)
    {
        return a*b;
    }

    public double impartireNumere(int a, int b)
    {
        return (double) a/b;
    }
}
